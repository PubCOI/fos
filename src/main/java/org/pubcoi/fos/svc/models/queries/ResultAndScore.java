package org.pubcoi.fos.svc.models.queries;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;

// used to store a document ID and its search response score
public class ResultAndScore {

    String id;
    Float score;

    ResultAndScore() {
    }

    public ResultAndScore(String id, float score) {
        this.id = id;
        this.score = score;
    }

    public String getId() {
        return id;
    }

    public ResultAndScore setId(String id) {
        this.id = id;
        return this;
    }

    public Float getScore() {
        return score;
    }

    public ResultAndScore setScore(Float score) {
        this.score = score;
        return this;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;

        if (o == null || getClass() != o.getClass()) return false;

        ResultAndScore that = (ResultAndScore) o;

        return new EqualsBuilder()
                .append(id, that.id)
                .isEquals();
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder(17, 37)
                .append(id)
                .toHashCode();
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this)
                .append("id", id)
                .append("score", score)
                .toString();
    }
}
